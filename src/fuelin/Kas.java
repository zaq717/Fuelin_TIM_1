            PreparedStatement ps = conn.prepareStatement(sql);

            // Set parameter untuk query SQL
            ps.setString(1, jumlah);
            ps.setString(2, catatan);
            ps.setString(3, jenis);
            ps.setString(4, currentDateTime);  // tanggal saat ini

