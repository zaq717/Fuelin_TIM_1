    void tampilTabel() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Jumlah");
        model.addColumn("Catatan");
        model.addColumn("Jenis");
        model.addColumn("Tanggal");

        String sql = "SELECT*FROM kas";
        try {
            Connection conn = Koneksi.konek();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                int id = rs.getInt("id_kas");
                String jumlah = rs.getString("jumlah");
                String catatan = rs.getString("catatan");
                String jenis = rs.getString("jenis");
                String tgl = rs.getString("waktu");
                Object[] baris = {id, jumlah, catatan, jenis, tgl};
                model.addRow(baris);
            }
        } catch (SQLException sQLException) {
            JOptionPane.showMessageDialog(null, "Gagal Memuat Data");
        }
        tbKas.setModel(model);
    }

    void tampilRingkasan() {
        String sql = "SELECT total_pendapatan, total_pengeluaran, saldo FROM v_ringkasan_kas";
        try {
            Connection conn = Koneksi.konek();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            if (rs.next()) {
                double totalPendapatan = rs.getDouble("total_pendapatan");
                double totalPengeluaran = rs.getDouble("total_pengeluaran");
                double saldo = rs.getDouble("saldo");
                DecimalFormat formatter = new DecimalFormat("#,###.00");

                txtPendapatan.setText(formatter.format(totalPendapatan));
                txtPengeluaran.setText(formatter.format(totalPengeluaran));
                txtSaldo.setText(formatter.format(saldo));
            }
        } catch (SQLException sQLException) {
            JOptionPane.showMessageDialog(null, "Gagal Memuat Data: " + sQLException.getMessage());
        }
    }

